                    render: function (data, type, full, meta) {
                        action = '<a href="' + site_url + 'admin/payments/delete/' + full.id + '" class="btn border-danger text-danger btn-flat btn-icon btn-rounded btn-xs" onclick="return confirm_alert(this)" title="Delete Business"><i class="icon-cross2"></i></a>';
